    void win(boolean b) {
        if (b != false) {
            victory.setVisible(true);
            exp.setText("" + fight.foe.getMaxHP());
            gold.setText("" + ((fight.foe.getMaxMP() * 3) / 2));
            AkashicTrials.hero.exp += fight.foe.getMaxHP();
            AkashicTrials.hero.gold += (fight.foe.getMaxMP() * 3) / 2;
            if (AkashicTrials.hero.getEXP() >= AkashicTrials.hero.getExpTo()) {
                levelUpButton.setVisible(true);
            }
        } else if (b == true) {
            defeat.setVisible(true);
        }
    }

    void run(boolean b) {
        if (b = true) {
            escape.setVisible(true);
        }
    }

